        },
        methods:{
            menu_select(item){
                switch (item) {
                    case 1: this.$router.replace('/home');break;
                    case 2: this.$router.replace('/video');break;
                    case 3: this.$router.replace('/information');break;
                    case 4: this.$router.replace('/user');break;
                }
                // this.menu_show = item
            }
        },
        mounted() {
            let menu_select = ['home','video','information','user']
            let url_str = this.$route.path
            let url_arr = url_str.split('/');
            for (let i =0; i<url_arr.length;i++){
                for (let j =0;j<menu_select.length;j++ ){
                    if (url_arr[i] == menu_select[j]){
                        this.menu_show = j+1
                    }
                }
            }
        }
    }
